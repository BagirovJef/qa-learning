    // === РЕНДЕР ВКЛАДОК ===
    function renderTabs() {
        const tabs = Object.entries(theorySections).map(([key, section]) => 
            `<button class="tab ${key === currentSection ? 'active' : ''}" data-section="${key}">${section.name}</button>`
        ).join('');
        
        const modeTabs = `
            <button class="mode-tab ${currentMode === 'theory' ? 'active' : ''}" data-mode="theory">📖 Теория</button>
            <button class="mode-tab ${currentMode === 'flashcards' ? 'active' : ''}" data-mode="flashcards">❓ Флеш-карточки</button>
        `;
        
        sectionTabs.innerHTML = `<div class="tabs">${tabs}</div><div class="mode-tabs">${modeTabs}</div>`;
        
        // Обработчики вкладок
        sectionTabs.querySelectorAll('.tab').forEach(tab => {
            tab.addEventListener('click', () => {
                currentSection = tab.dataset.section;
                renderTabs();
                renderContent();
            });
        });
        
        sectionTabs.querySelectorAll('.mode-tab').forEach(tab => {
            tab.addEventListener('click', () => {
                currentMode = tab.dataset.mode;
                renderTabs();
                renderContent();
            });
        });
    }
    
    // === РЕНДЕР КОНТЕНТА ===
    function renderContent() {
        if (currentMode === 'theory') {
            renderTheoryCards();
        } else {
            renderFlashcards();
        }
    }
    
    // === РЕНДЕР ТЕОРИИ ===
    function renderTheoryCards() {
        const section = theorySections[currentSection];
        app.innerHTML = section.cards.map((card, index) => `
            <div class="card" data-index="${index}">
                <div class="card-icon">${card.icon}</div>
                <div class="card-title">${card.title}</div>
                <div class="card-desc">${card.desc}</div>
            </div>
        `).join('');
    }
    
    // === РЕНДЕР ФЛЕШ-КАРТОЧЕК ===
    function renderFlashcards() {
        const category = flashcards.find(f => {
            const sectionKey = Object.keys(theorySections).find(key => theorySections[key].name === f.category);
            return sectionKey === currentSection;
        });
        
        if (!category) {
            app.innerHTML = '<p class="no-cards">Флеш-карточки для этого раздела пока не созданы</p>';
            return;
        }
        
        app.innerHTML = category.questions.map((q, index) => `
            <div class="flashcard" data-index="${index}">
                <div class="flashcard-front">
                    <div class="flashcard-icon">❓</div>
                    <div class="flashcard-question">${q.q}</div>
                </div>
                <div class="flashcard-back">
                    <div class="flashcard-icon">💡</div>
                    <div class="flashcard-answer">${q.a}</div>
                </div>
            </div>
        `).join('');
        
        // Обработчики переворота карточек
        app.querySelectorAll('.flashcard').forEach(card => {
            card.addEventListener('click', () => {
                card.classList.toggle('flipped');
            });
        });
    }
    
