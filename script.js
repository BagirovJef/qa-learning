    // === РЕНДЕР ФЛЕШ-КАРТОЧЕК ===
    function renderFlashcards() {
        const category = flashcards.find(f => {
            const sectionKey = Object.keys(theorySections).find(key => theorySections[key].name === f.category);
            return sectionKey === currentSection;
        });
        
        if (!category) {
            app.innerHTML = '<p class="no-cards">Флеш-карточки для этого раздела пока не созданы</p>';
            return;
        }
        
        app.innerHTML = category.questions.map((q, index) => `
            <div class="flashcard" data-index="${index}">
                <div class="flashcard-inner">
                    <div class="flashcard-front">
                        <div class="flashcard-icon">❓</div>
                        <div class="flashcard-question">${q.q}</div>
                        <div class="flashcard-hint">↻ Нажмите для ответа</div>
                    </div>
                    <div class="flashcard-back">
                        <div class="flashcard-icon">💡</div>
                        <div class="flashcard-answer">${q.a}</div>
                    </div>
                </div>
            </div>
        `).join('');
        
        // Обработчики переворота карточек
        app.querySelectorAll('.flashcard').forEach(card => {
            card.addEventListener('click', () => {
                card.classList.toggle('flipped');
            });
        });
    }
    
